package com.syntax.class8;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		// i want say good morning 5 times.
		
		for(int i = 1 ; i<=5 ; i++) {
			System.out.println("Goog Morning");
		}
		
		for ( int x = 1 ; x <= 100; x++) {
			System.out.print(x+" ");
		}
		
		System.out.println();
		
		for (int v = 10 ; v >=0 ; v--) {
			System.out.print(v+" ");
		}
		System.out.println();
		
		for (int d = 0 ; d <=30; d+=3) {
			System.out.print(d+" ");
		}
	}

}
